package utrzymanie;

public class Wynejem {
	
	private double czynsz;
	private double prad;
	private double internet;
	private double innerach;
	private double sumaWynajmu;
	
	
	
	public double getSumaWynajmu() {
		return sumaWynajmu;
	}
	public void setSumaWynajmu(double sumaWynajmu) {
		this.sumaWynajmu = sumaWynajmu;
	}
	public double getCzynsz() {
		return czynsz;
	}
	public void setCzynsz(double czynsz) {
		this.czynsz = czynsz;
	}
	public double getPrad() {
		return prad;
	}
	public void setPrad(double prad) {
		this.prad = prad;
	}
	public double getInternet() {
		return internet;
	}
	public void setInternet(double internet) {
		this.internet = internet;
	}
	public double getInnerach() {
		return innerach;
	}
	public void setInnerach(double innerach) {
		this.innerach = innerach;
	}
	
	
	

}
